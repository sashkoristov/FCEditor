class DataEval {
    constructor() {
        this.name = '';
        this.type = '';
        this.source = '';
    }

    getName() {
        return this.name;
    }

    setName(name) {
        this.name = name;
    }

    getType() {
        return this.type;
    }

    setType(type) {
        this.type = type;
    }

    getSource() {
        return this.source;
    }
}

export default DataEval;